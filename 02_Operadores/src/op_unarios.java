public class op_unarios {
    public static void main(String[] args) {
        // Operadores unários
        int a = 10;
        int b = 5;

        // Incremento
        a++; // a agora é 11

        // Decremento
        b--; // b agora é 4

        // Negação
        boolean c = true;
        c = !c; // c agora é false

        System.out.println("Valor final de a: " + a); // Imprime o valor final de 'a'
        System.out.println("Valor final de b: " + b); // Imprime o valor final de 'b'
        System.out.println("Valor final de c: " + c); // Imprime o valor final de 'c'
    }
}
