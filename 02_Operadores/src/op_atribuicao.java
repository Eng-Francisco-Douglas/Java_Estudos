public class op_atribuicao {
    public static void main(String[] args) {
        // Operadores de atribuição
        int a = 10;
        int b = 5;

        // Atribuição simples
        a = b; // a agora é 5

        // Atribuição com adição
        a += b; // a agora é 10 (5 + 5)

        // Atribuição com subtração
        a -= b; // a agora é 5 (10 - 5)

        // Atribuição com multiplicação
        a *= b; // a agora é 25 (5 * 5)

        // Atribuição com divisão
        a /= b; // a agora é 5 (25 / 5)

        // Atribuição com módulo
        a %= b; // a agora é 0 (5 % 5)

        System.out.println("Valor final de a: " + a); // Imprime o valor final de 'a'
    }
    
}
