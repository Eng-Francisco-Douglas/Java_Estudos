public class incremento_decremento {
    public static void main(String[] args) {
        // Incremento e Decremento
        int a = 10; // Inicializa a com 10
        System.out.println("Valor inicial de a: " + a); // Imprime o valor inicial de a

        // Incremento
        a++; // Incrementa a em 1, agora a é 11
        System.out.println("Valor de a após incremento: " + a); // Imprime o valor após incremento

        // Decremento
        a--; // Decrementa a em 1, agora a é 10 novamente
        System.out.println("Valor de a após decremento: " + a); // Imprime o valor após decremento
    }
}
