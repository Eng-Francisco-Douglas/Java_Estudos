public class calculadora_simples {
    public static void main(String[] args) {
        // Operadores aritméticos
        int a = 10;
        int b = 5;

        // Adição
        int soma = a + b; // soma é 15

        // Subtração
        int subtracao = a - b; // subtracao é 5

        // Multiplicação
        int multiplicacao = a * b; // multiplicacao é 50

        // Divisão
        int divisao = a / b; // divisao é 2

        // Módulo (resto da divisão)
        int modulo = a % b; // modulo é 0

        System.out.println("Soma: " + soma); // Imprime o resultado da soma
        System.out.println("Subtração: " + subtracao); // Imprime o resultado da subtração
        System.out.println("Multiplicação: " + multiplicacao); // Imprime o resultado da multiplicação
        System.out.println("Divisão: " + divisao); // Imprime o resultado da divisão
        System.out.println("Módulo: " + modulo); // Imprime o resultado do módulo
    }
}
