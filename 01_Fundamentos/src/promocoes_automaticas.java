public class promocoes_automaticas {
    public static void main(String[] args) {
        // Promocao automatica em Java
        // byte, short, int, long, float, double, char, boolean

        // byte para int
        byte b = 10;
        int i = b; // promocao automatica
        System.out.println("byte para int: " + i);

        // short para int
        short s = 20;
        i = s; // promocao automatica
        System.out.println("short para int: " + i);

        // char para int
        char c = 'A';
        i = c; // promocao automatica
        System.out.println("char para int: " + i);

        // int para long
        long l = i; // promocao automatica
        System.out.println("int para long: " + l);

        // float para double
        float f = 3.14f;
        double d = f; // promocao automatica
        System.out.println("float para double: " + d);
    }
}
