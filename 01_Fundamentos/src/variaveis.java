/**
 * Classe que demonstra o uso de variáveis em Java.
 * Este exemplo mostra como declarar uma variável inteira e imprimir seu valor.
 * 
 * @author Desenvolvedor
 * @version 1.0
 */
public class variaveis {
    /**
     * Método principal que é executado quando o programa inicia.
     * Demonstra a declaração de uma variável inteira e a impressão de seu valor.
     * 
     * @param args Argumentos de linha de comando (não utilizados neste exemplo)
     */
    public static void main(String[] args){
        // Declara uma variável inteira chamada 'farinha' e atribui o valor 2
        int farinha = 2;

        // Imprime a quantidade de farinha usando concatenação de strings
        System.out.println("Quantidade de farinhas: " + farinha);
    }
}
